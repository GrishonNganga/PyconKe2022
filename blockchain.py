def create_account(private_key = None):
    if private_key:
        acct = web3.eth.account.privateKeyToAccount(private_key)
        print(acct)
        return acct.address
    else:
        account = web3.eth.account.create()
        return format_address(account.address), web3.toHex(account.privateKey)

def get_eth_balance(public_key):
    return Web3.fromWei(web3.eth.getBalance(public_key), 'ether')

def format_address(address):
    return web3.toChecksumAddress(address)

def get_total_supply():
    total_supply = pycon_nft_contract.functions.totalSupply().call()
    print(total_supply)

def mint_price():
    return Web3.fromWei(pycon_nft_contract.functions.MINT_PRICE().call(), 'ether')

def nfts_for_user(public_key):
	return pycon_nft_contract.functions.balanceOf(public_key).call()

def purchase_nft(nft_ipfs_hash, public_key, private_key):
	try:
		tx = pycon_nft_contract.functions.safeMint(public_key, nft_ipfs_hash).buildTransaction({'nonce': web3.eth.get_transaction_count(public_key), 'value': web3.toWei(mint_price(), "ether"), "gasPrice": web3.eth.gas_price})
		signed_tx = web3.eth.account.signTransaction(tx, private_key=private_key)
		tx_hash = web3.eth.sendRawTransaction(signed_tx.rawTransaction)
		tx_result = web3.eth.get_transaction_receipt(tx_hash)
		print(tx_result)
		return True, "Success"
	except Exception as e:
		return None, str(e)

def nfts_for_user(public_key):
	for i in range(1, pycon_nft_contract.functions.totalSupply().call()):
		print(i)
	user_nfts = [ {'id': i, 'img': pycon_nft_contract.functions.tokenURI(i).call()} for i in range(1, pycon_nft_contract.functions.totalSupply().call() + 1) if pycon_nft_contract.functions.ownerOf(i).call() == public_key]
	return user_nfts
